E='\033[0;37m~~~~~\033[0m'
N='\033[0m'

frames=(
   " 
   |_‾___|_____|___‾_|_____|
   |$I|[‾‾‾]|[‾‾‾]|$C|
   |$I|  |  |  |  |$C|
   |[‾‾‾]|  |  |  |  |[‾‾‾]|
   _  |  _|‾‾‾|_|‾‾‾|_  |  _
    |_|_|             |_|_|
   "

   "
   |_____|_____|_‾___|___‾_|
   |     |$C|$I|$E|
   |[‾‾‾]|[‾‾‾]|[‾‾‾]|[‾‾‾]|
   |  |  |  |  |  |  |  |  |
   ___|_____|_____|_____|__
  
   "

   "
   |_____|_____|_‾___|___‾_|
   |[‾‾‾]|$C|$I|[‾‾‾]|
   |  |  |$C|$I|  |  |
   |  |  |[‾‾‾]|[‾‾‾]|  |  |
   _|‾‾‾|_  |  _  |  _|‾‾‾|_
          |_|_| |_|_|
   "

   "
   |_____|___‾_|_____|_‾___|
   |$C|$E|     |$I|
   |[‾‾‾]|[‾‾‾]|[‾‾‾]|[‾‾‾]|
   |  |  |  |  |  |  |  |  |
   ___|_____|_____|_____|___
  
   "

   " 
   |_____|___‾_|_____|_‾___|
   |$C|[‾‾‾]|[‾‾‾]|$I|
   |$C|  |  |  |  |$I|
   |[‾‾‾]|  |  |  |  |[‾‾‾]|
   _  |  _|‾‾‾|_|‾‾‾|_  |  _
    |_|_|             |_|_|
   "

   "
   |___‾_|_‾___|_____|_____|
   |$E|$I|$C|     |
   |[‾‾‾]|[‾‾‾]|[‾‾‾]|[‾‾‾]|
   |  |  |  |  |  |  |  |  |
   ___|_____|_____|_____|___
  
   "

   "
   |___‾_|_‾___|_____|_____|
   |[‾‾‾]|$I|$C|[‾‾‾]|
   |  |  |$I|$C|  |  |
   |  |  |[‾‾‾]|[‾‾‾]|  |  |
   _|‾‾‾|_  |  _  |  _|‾‾‾|_
          |_|_| |_|_|
   "

   "
   |_‾___|_____|___‾_|_____|
   |$I|     |$E|$C|
   |[‾‾‾]|[‾‾‾]|[‾‾‾]|[‾‾‾]|
   |  |  |  |  |  |  |  |  |
   ___|_____|_____|_____|__
  
   "
 )

while :
do
    for (( frame = 0; frame < ${#frames[@]}; frame++ ))
    do
        clear
        
        f=${frames[frame]}

        echo -e "\r$f"

        sleep $frame_time
    done
done
